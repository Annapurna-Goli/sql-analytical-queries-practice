-- SELF JOIN

SELECT B.NAME AS TEACHER_NAME,S.NAME AS STUDENT_NAME,S.NUMBER_COURSES FROM STUDENTS S
JOIN STUDENTS B
ON S.TEACHER_ID=B.ROLL_NUM
ORDER BY NUMBER_COURSES DESC;
