-- LEFT JOIN 
SELECT NAME,ROLL_NUM,MARKS FROM STUDENTS S
LEFT JOIN MARKS_RESULT M
ON S.ROLL_NUM=M.HALL_TICKIT_NUMBER;

-- RIGHT JOIN
SELECT * FROM STUDENTS S
RIGHT JOIN MARKS_RESULT M
ON S.ROLL_NUM=M.HALL_TICKIT_NUMBER;


-- INNER JOIN
SELECT * FROM STUDENTS S
INNER JOIN MARKS_RESULT M
ON S.ROLL_NUM=M.HALL_TICKIT_NUMBER;

-- FULL/OUTER JOIN
SELECT * FROM STUDENTS S
LEFT JOIN MARKS_RESULT M
ON S.ROLL_NUM=M.HALL_TICKIT_NUMBER
UNION 
SELECT * FROM STUDENTS S
RIGHT JOIN MARKS_RESULT M
ON S.ROLL_NUM=M.HALL_TICKIT_NUMBER;

-- CROSS JOIN(WITH THE HEP OF CROSS JOIN METHOD)
SELECT * FROM STUDENTS
CROSS JOIN MARKS_RESULT;

-- CROSS JOIN(WITH THE HELP OF DIRECT METHOD)
SELECT * FROM STUDENTS,MARKS_RESULT;

-- EXCLUSIVE LEFT JOIN
SELECT * FROM STUDENTS S
LEFT JOIN MARKS_RESULT M
ON S.ROLL_NUM=M.HALL_TICKIT_NUMBER
WHERE M.HALL_TICKIT_NUMBER IS NULL;

-- EXCLUSIVE RIGHT JOIN
SELECT * FROM STUDENTS S
RIGHT JOIN MARKS_RESULT M
ON S.ROLL_NUM=M.HALL_TICKIT_NUMBER
WHERE S.ROLL_NUM IS NULL;

