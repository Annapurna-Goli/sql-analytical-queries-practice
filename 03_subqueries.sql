WHERE ROLL_NUM IN (
SELECT HALL_TICKIT_NUMBER FROM MARKS_RESULT
WHERE MARKS>(SELECT AVG(MARKS) FROM MARKS_RESULT)
);

-- LESS THAN HIGHEST MARKS
SELECT S.NAME,B.MARKS FROM STUDENTS S
JOIN MARKS_RESULT B
ON S.ROLL_NUM=B.HALL_TICKIT_NUMBER
WHERE MARKS<(SELECT MAX(MARKS) FROM MARKS_RESULT);

-- HIGHEST MARKS
SELECT S.NAME,B.MARKS FROM STUDENTS S
JOIN MARKS_RESULT B
ON S.ROLL_NUM=B.HALL_TICKIT_NUMBER
WHERE MARKS=(SELECT MAX(MARKS) FROM MARKS_RESULT);

-- 2ND HIGHEST MARKS
SELECT S.NAME,B.MARKS FROM STUDENTS S
JOIN MARKS_RESULT B
ON S.ROLL_NUM=B.HALL_TICKIT_NUMBER
WHERE MARKS IN (SELECT MAX(MARKS) FROM MARKS_RESULT
WHERE MARKS NOT IN (SELECT MAX(MARKS) FROM MARKS_RESULT));

